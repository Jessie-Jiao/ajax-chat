package bean;

/**
 *
 * @author Siwei Jiao
 */
public class CustServBean {
    private int custServ_id;
    private String user_name;
    private String first_name;
    private String last_name;
 

    /**
     * @return the custServ_id
     */
    public int getCustServ_id() {
        return custServ_id;
    }

    /**
     * @param custServ_id the custServ_id to set
     */
    public void setCustServ_id(int custServ_id) {
        this.custServ_id = custServ_id;
    }

    /**
     * @return the user_name
     */
    public String getUser_name() {
        return user_name;
    }

    /**
     * @param user_name the user_name to set
     */
    public void setUser_name(String user_name) {
        this.user_name = user_name;
    }

    /**
     * @return the first_name
     */
    public String getFirst_name() {
        return first_name;
    }

    /**
     * @param first_name the first_name to set
     */
    public void setFirst_name(String first_name) {
        this.first_name = first_name;
    }

    /**
     * @return the last_name
     */
    public String getLast_name() {
        return last_name;
    }

    /**
     * @param last_name the last_name to set
     */
    public void setLast_name(String last_name) {
        this.last_name = last_name;
    }

   
}
